    private void iniciarCalculos(){
       Drop d = new Drop();
       Producer p1 = new Producer(d, "p1");
       p1.addObserver(this);
       Thread tp1 = new Thread(p1, "tp1");
       Producer p2 = new Producer(d, "p2");
       p2.addObserver(this);
       Thread tp2 = new Thread(p2, "tp2");
       Producer p3 = new Producer(d, "p3");
       p3.addObserver(this);
       Thread tp3 = new Thread(p3, "tp3");
       Consumer c1 = new Consumer(d, "c1");
       c1.addObserver(this);
       Thread tc1 = new Thread(c1, "tc1");
       Consumer c2 = new Consumer(d, "c2");
       c2.addObserver(this);
       Thread tc2 = new Thread(c2, "tc2");
       Thread[] ts = {tp1, tp2, tp3, tc1, tc2};
       this.mt = new MedicionTiempo(ts);
       this.mt.addObserver(this);
       Thread tmt = new Thread(this.mt);
       tmt.start();
       threads = ts;
    }
    
