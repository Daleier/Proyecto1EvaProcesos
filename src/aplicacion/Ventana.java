    @Override
    public void update(Observable o, Object arg) {
        String observ = o.getClass().getSimpleName();
        
        if(observ.equalsIgnoreCase("MedicionTiempo")){
            java.awt.EventQueue.invokeLater(new Runnable() {
                public void run() {
                        for(Object j : Recursos.jornadas){
                        Jornada jor = (Jornada) j;
                        if(jor.getIdParque() > 0){
                            jTextAreaJornadas.append("ID: "+jor.getIdParque() + " - Horas: "+jor.getNumVisitantes()+"\n");
                        }
                    }
                    for(Object e : Recursos.empleados){
                        Empleado emp = (Empleado) e;
                        if(emp.getId() > 0){
                            jTextAreaEmpleados.append("ID: "+emp.getId() + " - Horas Totales: "+emp.getVisitantesTotales()+"\n");
                        }
                    }
                    jLabelTime.setText(arg.toString());
                }
            });
        }else if (observ.equalsIgnoreCase("Consumer")){
            if(arg != null){
                java.awt.EventQueue.invokeLater(new Runnable() {
                    public void run() {
                        jTextAreaConsumer.append(arg.toString()+"\n");
                    }
                });
            }else{
                interrumpirThreads();
            }
        }else if (observ.equalsIgnoreCase("Producer")){
            if(arg != null){
                java.awt.EventQueue.invokeLater(new Runnable() {
                    public void run() {
                        jTextAreaProducer.append(arg.toString()+ "\n");
                    }
                });
            }else{
                interrumpirThreads();
            }            
        }else{
            if(arg == null){
                interrumpirThreads();
            }
        }
    }

    synchronized void interrumpirThreads() {
        for (Thread t : this.threads) {
            if (t.isAlive()) {
                t.interrupt();
                System.out.println("thread: " + t.getName());
            }
        }
    } 
    
    
    
